OSU.Trans = "en"

function OSU:LookupTranslate(index)
	if(OSU.Lang[index] == nil) then return "Undefined" end
	local ret = OSU.Lang[index][OSU.Trans]
	if(ret == nil) then
		return OSU.Lang[index]["en"]
	end
	return ret
end